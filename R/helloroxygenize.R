#' Add together three numbers
#'
#' @param x number
#' @param y another number
#' @param  y1 a third number
#' @return The sum of all three numbers
math2<-function(x,y,y1){
  z=x+y+y1
  return(z)
}



#' Add together two numbers
#'
#' @param x number
#' @param y another number
#' @return The sum of both numbers
math<-function(x,y){
  z=x+y
  return(z)
}
